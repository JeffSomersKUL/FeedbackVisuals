export function getSuperTopColor(opacity: number = 1): string {
    return `rgba(0,100,0, ${opacity})`
}

export function getTopColor(opacity: number = 1): string {
    return `rgba(144,238,144, ${opacity})`
}

export function getMiddleColor(opacity: number = 1): string {
    return `rgba(255,215,0, ${opacity})`
}

export function getBottomColor(opacity: number = 1): string {
    return `rgba(255, 140, 0, ${opacity})`
}

export function getSuperBottomColor(opacity: number = 1): string {
    return `rgba(255,165,0, ${opacity})`
}

export function getUnknownColor(opacity: number = 1): string {
    return `rgb(100, 100, 100, ${opacity})`
}